public class dataclass {
    public static void main(String[] args) {
        byte a = 10;
        System.out.println(a);
        byte b = 10;
        System.out.println(b);
        byte c = (byte)(b+a);  // {target data-type variable name}
        System.out.println(c);
        int s = 5;
        long q = 10;
        long d = s+q;
        System.out.println(d);
    }
}
